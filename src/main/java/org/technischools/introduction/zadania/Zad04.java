package org.technischools.introduction.zadania;

/*
Napisz pętlę while, która wypisze na ekranie wszystkie liczby od 10 do 20 włącznie.
*/

public class Zad04 {
    public static void main(String[] args) {
        int i = 10;
        while (i <= 20) {
            System.out.println(i);
            i++;
        }
    }
}
